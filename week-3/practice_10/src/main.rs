fn main() {
    
    //addition
    let sum = 5500 + 7310;
    println!("The sum of 5500 and 7310 is {}", sum);

    //subtraction
    let difference:f32 = 95.5 - 4.3;
    println!("The difference between 95.5 and 4.3 is {}", difference);

    //multiplication
    let product:f32 = 4.0 * 30.0;
    println!("The multiple of 4 and 30 is {}", product);

    //division
    let quotient:f32 = 56.7 / 32.2;
    println!("The quotient of 56.7 and 32.2 is {}", quotient);

    //remainder
    let remainder:f32 = 43.0 % 5.0;
    println!("The remainder of 43 and 5 is {}", remainder);
}
