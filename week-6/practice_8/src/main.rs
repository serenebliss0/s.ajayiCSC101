fn main()
{
    let num1 = 10;
    let num2 = 2;
    let mut result:i32;

    result = num1 + num2;
    println!("Sum is {}", result);

    result = num1 - num2;
    println!("Difference is {}", result);

    result = num1 * num2;
    println!("Product is {}", result);

    result = num1 / num2;
    println!("Quotient is {}", result);

    result = num1 % num2;
    println!("Remainder is {}", result);
}